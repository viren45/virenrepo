package Day19_Operators;

public class ArithmaticOperators {

	public static void main(String[] args) {
	
	int	num1=13, num2=4;
	//Addition opration
	int res=num1+num2;
	System.out.println("Sum value is: "+res);
	
	//Subtraction operation
	res=num1-num2;
	System.out.println("\nDifference value is: "+res);
	
	//Multiplication operation
	res=num1*num2;
	System.out.println("\nMultiplied value is: "+res);
	
	//Division operation
	res=num1/num2;
	System.out.println("\nDivision value is: "+res);
	
	//Modulus operation
	res=num1%num2;
	System.out.println("\nreminder value is: "+res);

	}

}
