package Day23_SwitchCase;

public class SwitchClass2 {

	public static void main(String[] args) {
	
	int value = 6;
	switch (value) {
	
	case 1:
		System.out.println("Hi , This Month is January");
	break;
	
	case 2:
		System.out.println("Hi , This Month is February");
	break;
	
	case 3:
		System.out.println("Hi , This Month is March");
	break;
	
	case 4:
		System.out.println("Hi , This Month is April");
	break;
	
	case 5:
		System.out.println("Hi , This Month is May");
	break;
	
	case 6:
		System.out.println("Hi , This Month is June");
	break;
	
	case 7:
		System.out.println("Hi , This Month is July");
	break;
	
	case 8:
		System.out.println("Hi , This Month is August");
	break;
	
	case 9:
		System.out.println("Hi , This Month is September");
	break;
	
	case 10:
		System.out.println("Hi , This Month is October");
	break;
	
	case 11:
		System.out.println("Hi , This Month is November");
	break;
	
	case 12:
		System.out.println("Hi , This Month is December");
	break;
	
	default :
		System.out.println("Sorry this number of month is not fount");
	break;
	}
	}

}
